    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        // Boton Acceptar
        

        jTextAreaResultado.setText(
                jTextAreaResultado.getText()+
            "Nombre: "+jTextFieldNombre.getText()+"\n"+
            "Apellido: "+jTextFieldApellido.getText()+"\n"+
            "DNI: "+jFormattedTextFieldDNI.getText()+"\n"+
            "NÂº Cuenta: "+jFormattedTextFielDNCuenta.getText()+"\n"+
            "Telefono: "+jFormattedTextFielTelefono.getText()+"\n\n\n\n"
        );
        
        //Limpio los campos del usuario
        limpiarCampos();

    }//GEN-LAST:event_jButtonAceptarActionPerformed

    private void comprobarTodosCamposActivados(){
        
        if (("".equals(jTextFieldNombre.getText())==false) &&
                ("".equals(jTextFieldApellido.getText())==false) &&
                    jFormattedTextFieldDNI.isValid() &&
                        jFormattedTextFielDNCuenta.isValid() &&
                            jFormattedTextFielTelefono.isValid()){
            
            jButtonAceptar.setEnabled(true);
        }else{
            jButtonAceptar.setEnabled(false);
        }
        
    }
    
    private void limpiarCampos() {
        // TODO add your handling code here:
        
        jTextFieldNombre.setText("");
        jTextFieldApellido.setText("");
        jFormattedTextFieldDNI.setValue(null);
        jFormattedTextFielDNCuenta.setValue(null);
        jFormattedTextFielTelefono.setValue(null);
    }
    
